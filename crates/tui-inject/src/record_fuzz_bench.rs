use std::fs;
use std::panic;
use std::process::ExitCode;
use std::time::{Duration, Instant};

use crossterm::event::{Event, KeyCode, KeyEvent, KeyEventKind, KeyEventState, KeyModifiers};

use crate::widget::WidgetSpec;

// ============================================================
// record
// ============================================================

/// `tui-inject record` — capture keyboard events and save as TOML.
///
/// If the process has a controlling TTY, enters raw mode and records keys
/// interactively until `q` or `Ctrl+C` is pressed. If no TTY is available
/// (e.g. CI, piped stdin), writes a skeleton scenario file that the user
/// can edit.
pub fn record(output: &str) -> ExitCode {
    if !is_tty() {
        let toml = write_skeleton();
        match fs::write(output, toml) {
            Ok(()) => {
                eprintln!("no TTY detected — wrote skeleton scenario to {output}");
                eprintln!("edit it manually, then run `tui-inject replay {output}`");
                ExitCode::SUCCESS
            }
            Err(e) => {
                eprintln!("write error: {e}");
                ExitCode::from(1)
            }
        }
    } else {
        record_interactive(output)
    }
}

/// Check if stdin is a TTY (best-effort, no extra dependency).
fn is_tty() -> bool {
    // We use crossterm's tty detection indirectly: try enable_raw_mode and
    // see if it succeeds. If it errors, we're not on a TTY.
    crossterm::terminal::enable_raw_mode().is_ok()
}

/// Build the skeleton scenario TOML content (without writing it).
fn write_skeleton() -> String {
    r#"# Skeleton scenario generated by `tui-inject record`.
# No controlling TTY was available, so this is a template — edit the events
# list manually, then run `tui-inject replay`.

[widget]
name = "counter"
initial = 0

# Events: each [[events]] block is one input.
# Simple form:
#   [[events]]
#   key = "enter"
#
# Modifier form:
#   [[events]]
#   key = "ctrl+c"
#
# Full form (with explicit modifiers list):
#   [[events]]
#   key = "a"
#   modifiers = ["shift"]

[[events]]
key = "+"

[[events]]
key = "+"

[[events]]
key = "q"

[output]
format = "text"
width = 40
height = 6
"#
    .to_string()
}

/// Interactive recording on a real TTY.
fn record_interactive(output: &str) -> ExitCode {
    use crossterm::event;
    use crossterm::event::{DisableMouseCapture, EnableMouseCapture};
    use crossterm::execute;
    use crossterm::terminal::{disable_raw_mode, EnterAlternateScreen, LeaveAlternateScreen};
    use std::io::stdout;

    // Raw mode was enabled by is_tty(); set up the rest.
    let mut stdout = stdout();
    let _ = execute!(stdout, EnterAlternateScreen, EnableMouseCapture);

    let mut events: Vec<EventRecord> = Vec::new();
    let mut instructions = [
        "Recording — press keys to capture. Press q / Esc / Ctrl+C to stop.",
        "Each key will be saved as a [[events]] entry in the output TOML.",
    ];
    let _ = instructions.iter_mut(); // silence dead-code warning on instructions

    loop {
        if let Ok(true) = event::poll(Duration::from_millis(100)) {
            if let Ok(Event::Key(key)) = event::read() {
                if key.kind != KeyEventKind::Press {
                    continue;
                }
                let stop = matches!(key.code, KeyCode::Char('q') | KeyCode::Esc)
                    || (key.modifiers.contains(KeyModifiers::CONTROL)
                        && key.code == KeyCode::Char('c'));

                if !stop {
                    events.push(EventRecord::from(&key));
                }
                if stop {
                    break;
                }
            }
        }
    }

    let _ = execute!(stdout, LeaveAlternateScreen, DisableMouseCapture);
    let _ = disable_raw_mode();

    let toml = format_scenario_toml(&events);
    match fs::write(output, toml) {
        Ok(()) => {
            eprintln!("recorded {} events to {output}", events.len());
            ExitCode::SUCCESS
        }
        Err(e) => {
            eprintln!("write error: {e}");
            ExitCode::from(1)
        }
    }
}

/// Compact representation of one captured key event, for TOML output.
struct EventRecord {
    key: String,
    modifiers: Vec<String>,
}

impl EventRecord {
    fn from(key: &KeyEvent) -> Self {
        let key_str = match key.code {
            KeyCode::Enter => "enter".to_string(),
            KeyCode::Tab => "tab".to_string(),
            KeyCode::Backspace => "backspace".to_string(),
            KeyCode::Delete => "delete".to_string(),
            KeyCode::Esc => "esc".to_string(),
            KeyCode::Up => "up".to_string(),
            KeyCode::Down => "down".to_string(),
            KeyCode::Left => "left".to_string(),
            KeyCode::Right => "right".to_string(),
            KeyCode::Home => "home".to_string(),
            KeyCode::End => "end".to_string(),
            KeyCode::PageUp => "pageup".to_string(),
            KeyCode::PageDown => "pagedown".to_string(),
            KeyCode::Char(' ') => "space".to_string(),
            KeyCode::Char(c) => c.to_string(),
            KeyCode::F(n) => format!("f{n}"),
            KeyCode::Null => "null".to_string(),
            other => format!("{other:?}"),
        };
        let mut modifiers = Vec::new();
        if key.modifiers.contains(KeyModifiers::SHIFT) {
            modifiers.push("shift".to_string());
        }
        if key.modifiers.contains(KeyModifiers::CONTROL) {
            modifiers.push("ctrl".to_string());
        }
        if key.modifiers.contains(KeyModifiers::ALT) {
            modifiers.push("alt".to_string());
        }
        if key.modifiers.contains(KeyModifiers::SUPER) {
            modifiers.push("super".to_string());
        }
        Self {
            key: key_str,
            modifiers,
        }
    }
}

/// Format a list of captured events as a complete scenario TOML string.
fn format_scenario_toml(events: &[EventRecord]) -> String {
    let mut out = String::new();
    out.push_str("# Recorded with `tui-inject record`.\n\n");
    out.push_str("[widget]\nname = \"counter\"\ninitial = 0\n\n");
    for ev in events {
        if ev.modifiers.is_empty() {
            out.push_str(&format!("[[events]]\nkey = \"{}\"\n\n", ev.key));
        } else {
            let mods = ev
                .modifiers
                .iter()
                .map(|m| format!("\"{m}\""))
                .collect::<Vec<_>>()
                .join(", ");
            out.push_str(&format!(
                "[[events]]\nkey = \"{}\"\nmodifiers = [{}]\n\n",
                ev.key, mods
            ));
        }
    }
    out.push_str("[output]\nformat = \"text\"\nwidth = 40\nheight = 6\n");
    out
}


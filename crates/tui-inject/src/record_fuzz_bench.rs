/// `tui-inject fuzz <widget> --events N` — inject N random events.
///
/// Drives a Counter App through N random key events, using `catch_unwind`
/// per event to isolate panics. Reports total events, panics, and final state.
pub fn fuzz(name: &str, event_count: usize) -> ExitCode {
    if name != "counter" {
        eprintln!("fuzz: widget '{name}' not supported (only 'counter' is fuzzable currently)");
        return ExitCode::from(2);
    }

    let mut rng = Prng::seeded(0xC0FFEE_u64);
    let mut panics = 0;
    let mut counter = CounterForFuzz::default();

    for i in 0..event_count {
        let event = random_key_event(&mut rng);
        let result = panic::catch_unwind(panic::AssertUnwindSafe(|| {
            counter.on_event(event);
        }));
        if result.is_err() {
            panics += 1;
            eprintln!("fuzz: panic at event {i}");
        }
    }

    println!("fuzz results for '{name}':");
    println!("  events generated: {event_count}");
    println!("  panics:           {panics}");
    println!("  final count:      {}", counter.count);
    if panics == 0 {
        println!("  status:           OK (no panics)");
        ExitCode::SUCCESS
    } else {
        println!("  status:           FAIL ({panics} panics)");
        ExitCode::from(1)
    }
}

/// Minimal xorshift PRNG — no external dependency.
struct Prng {
    state: u64,
}

impl Prng {
    fn seeded(seed: u64) -> Self {
        Self {
            state: if seed == 0 { 0xDEADBEEF } else { seed },
        }
    }

    fn next_u64(&mut self) -> u64 {
        let mut x = self.state;
        x ^= x << 13;
        x ^= x >> 7;
        x ^= x << 17;
        self.state = x;
        x
    }

    fn pick<'a, T>(&mut self, items: &'a [T]) -> &'a T {
        let idx = (self.next_u64() as usize) % items.len();
        &items[idx]
    }
}

/// Generate a random key event from a small, known-safe alphabet.
fn random_key_event(rng: &mut Prng) -> Event {
    let codes = [
        KeyCode::Char('+'),
        KeyCode::Char('-'),
        KeyCode::Up,
        KeyCode::Down,
        KeyCode::Char('r'),
        KeyCode::Char('q'),
        KeyCode::Esc,
        KeyCode::Enter,
        KeyCode::Tab,
        KeyCode::Char('a'),
    ];
    Event::Key(KeyEvent::new_with_kind_and_state(
        *rng.pick(&codes),
        KeyModifiers::NONE,
        KeyEventKind::Press,
        KeyEventState::NONE,
    ))
}

/// Minimal Counter for fuzzing — stateful, with the same Msg logic as the
/// demo app, but without the App/Model trait baggage (no Command plumbing).
#[derive(Debug, Default)]
struct CounterForFuzz {
    count: i32,
}

impl CounterForFuzz {
    fn on_event(&mut self, event: Event) {
        if let Event::Key(KeyEvent {
            code,
            kind: KeyEventKind::Press,
            ..
        }) = event
        {
            match code {
                KeyCode::Char('+') | KeyCode::Up => self.count += 1,
                KeyCode::Char('-') | KeyCode::Down => self.count -= 1,
                KeyCode::Char('r') => self.count = 0,
                _ => {}
            }
        }
    }
}


/// `tui-inject bench <widget> --iterations N` — measure render time.
///
/// Renders the widget N times via TestBackend and reports min/mean/p95/max.
pub fn bench(name: &str, iterations: usize) -> ExitCode {
    let spec = match WidgetSpec::from_name(name) {
        Some(s) => s,
        None => {
            eprintln!(
                "bench: unknown widget '{name}'. Available: {}",
                WidgetSpec::NAMES.join(", ")
            );
            return ExitCode::from(1);
        }
    };

    let width = 60_u16;
    let height = 16_u16;
    let mut timings: Vec<Duration> = Vec::with_capacity(iterations);

    for _ in 0..iterations {
        let backend = ratatui::backend::TestBackend::new(width, height);
        let mut terminal = ratatui::Terminal::new(backend).expect("terminal");
        let start = Instant::now();
        terminal
            .draw(|frame| spec.render(frame, ratatui::layout::Rect::new(0, 0, width, height)))
            .expect("draw");
        timings.push(start.elapsed());
    }

    timings.sort();
    let min = timings.first().copied().unwrap_or_default();
    let max = timings.last().copied().unwrap_or_default();
    let mean_ns = timings.iter().map(|d| d.as_nanos()).sum::<u128>() / iterations.max(1) as u128;
    let p95_idx = (iterations * 95) / 100;
    let p95 = timings
        .get(p95_idx.min(timings.len().saturating_sub(1)))
        .copied()
        .unwrap_or_default();

    println!("bench results for '{name}' ({iterations} iterations, {width}x{height}):");
    println!("  min:  {}", format_duration(min));
    println!(
        "  mean: {}",
        format_duration(Duration::from_nanos(mean_ns as u64))
    );
    println!("  p95:  {}", format_duration(p95));
    println!("  max:  {}", format_duration(max));
    ExitCode::SUCCESS
}

/// Format a Duration for human-friendly display.
fn format_duration(d: Duration) -> String {
    let ns = d.as_nanos();
    if ns < 1_000 {
        format!("{ns} ns")
    } else if ns < 1_000_000 {
        format!("{:.2} µs", ns as f64 / 1_000.0)
    } else {
        format!("{:.2} ms", ns as f64 / 1_000_000.0)
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn prng_is_deterministic() {
        let mut a = Prng::seeded(42);
        let mut b = Prng::seeded(42);
        for _ in 0..10 {
            assert_eq!(a.next_u64(), b.next_u64());
        }
    }

    #[test]
    fn prng_pick_returns_in_range() {
        let mut rng = Prng::seeded(7);
        let items = [1, 2, 3, 4, 5];
        for _ in 0..20 {
            let picked = rng.pick(&items);
            assert!(items.contains(picked));
        }
    }

    #[test]
    fn random_key_event_produces_known_codes() {
        let mut rng = Prng::seeded(99);
        for _ in 0..20 {
            let ev = random_key_event(&mut rng);
            assert!(matches!(ev, Event::Key(_)));
        }
    }

    #[test]
    fn counter_for_fuzz_increments_and_decrements() {
        let mut c = CounterForFuzz::default();
        c.on_event(random_key_event(&mut Prng::seeded(1)));
        // Just verify it doesn't panic on any random event.
        let mut rng = Prng::seeded(2);
        for _ in 0..100 {
            c.on_event(random_key_event(&mut rng));
        }
    }

    #[test]
    fn format_duration_handles_ranges() {
        assert_eq!(format_duration(Duration::from_nanos(500)), "500 ns");
        assert_eq!(format_duration(Duration::from_nanos(1_500)), "1.50 µs");
        assert_eq!(format_duration(Duration::from_nanos(1_500_000)), "1.50 ms");
    }

    #[test]
    fn format_scenario_toml_includes_widget_and_output() {
        let events = vec![EventRecord {
            key: "enter".to_string(),
            modifiers: vec![],
        }];
        let toml = format_scenario_toml(&events);
        assert!(toml.contains("[widget]"));
        assert!(toml.contains("[[events]]"));
        assert!(toml.contains("[output]"));
        assert!(toml.contains("enter"));
    }

    #[test]
    fn event_record_maps_chars_and_arrows() {
        let key = KeyEvent::new_with_kind_and_state(
            KeyCode::Char('a'),
            KeyModifiers::SHIFT,
            KeyEventKind::Press,
            KeyEventState::NONE,
        );
        let rec = EventRecord::from(&key);
        assert_eq!(rec.key, "a");
        assert_eq!(rec.modifiers, vec!["shift".to_string()]);

        let key = KeyEvent::new_with_kind_and_state(
            KeyCode::Up,
            KeyModifiers::NONE,
            KeyEventKind::Press,
            KeyEventState::NONE,
        );
        let rec = EventRecord::from(&key);
        assert_eq!(rec.key, "up");
        assert!(rec.modifiers.is_empty());
    }

    #[test]
    fn write_skeleton_includes_counter_widget() {
        let toml = write_skeleton();
        assert!(toml.contains("name = \"counter\""));
        assert!(toml.contains("[[events]]"));
    }
}
